import Head from 'next/head'
import { useRouter } from 'next/router';
import { useEffect } from 'react';

import * as S from "./styles";

export default function Home() {
  const router = useRouter();

  useEffect(() => {
    const options: IntersectionObserverInit = {
      root: document.querySelector("main"), rootMargin: "40px", threshold: 1
    };
    const observer = new IntersectionObserver(elements => {
      const [element] = elements;
      
      if (element.isIntersecting)
        router.replace(`/#${element.target.id}`, undefined, { shallow: true })
    }, options);

    Array.from(document.querySelectorAll("main > section")).forEach((element) => {
      observer.observe(element);
    })

    return () => {
      observer.disconnect()
    }
  }, []);

  return (
    <>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <S.Section id="who-am-I" $bgColor='red'></S.Section>
      <S.Section id="skills" $bgColor='green'></S.Section>
      <S.Section id="portfolio" $bgColor='blue'></S.Section>
    </>
  )
}
